// Lock state for each resize handle
const resizeLocks = { sidebar: false, chat: false, panel: false };

function initResizeHandles() {
    const sidebar = document.getElementById('sidebar');
    const sidebarHandle = document.getElementById('sidebar-resize');
    const chatPanel = document.getElementById('omega-chat-panel');
    const chatHandle = document.getElementById('chat-resize');
    const bottomPanel = document.getElementById('bottom-panel');
    const panelHandle = document.getElementById('panel-resize');

    makeResizable(sidebarHandle, 'sidebar', (dx) => {
        // If collapsed, un-collapse on drag
        if (sidebar.classList.contains('sidebar-collapsed')) {
            sidebar.classList.remove('sidebar-collapsed');
            sidebar.style.width = '40px';  // Start from small width
        }
        const w = Math.max(180, Math.min(500, sidebar.offsetWidth + dx));
        sidebar.style.width = w + 'px';
        state.editor?.layout();
    });

    makeResizable(chatHandle, 'chat', (dx) => {
        const w = Math.max(250, Math.min(600, chatPanel.offsetWidth - dx));
        chatPanel.style.width = w + 'px';
        state.editor?.layout();
    });

    makeResizable(panelHandle, 'panel', (_, dy) => {
        const h = Math.max(100, Math.min(600, bottomPanel.offsetHeight - dy));
        bottomPanel.style.height = h + 'px';
        fitAllTerminals();
    }, true);

    // Initialize lock buttons
    initResizeLocks();
}

function makeResizable(handle, lockKey, onDrag, isVertical = false) {
    let startX, startY;
    handle.addEventListener('mousedown', (e) => {
        // If locked, ignore drag (but allow lock button clicks)
        if (resizeLocks[lockKey]) return;
        // Don't start drag if clicking the lock button itself
        if (e.target.closest('.resize-lock-btn')) return;

        startX = e.clientX; startY = e.clientY;
        handle.classList.add('active');
        document.body.style.cursor = isVertical ? 'row-resize' : 'col-resize';
        document.body.style.userSelect = 'none';
        // v4.3.5: Prevent iframes/webviews from stealing mouse events during drag
        document.querySelectorAll('iframe, webview, .xterm').forEach(el => el.style.pointerEvents = 'none');
        const onMove = (e) => {
            const dx = e.clientX - startX; const dy = e.clientY - startY;
            startX = e.clientX; startY = e.clientY;
            onDrag(dx, dy);
        };
        const onUp = () => {
            handle.classList.remove('active');
            document.body.style.cursor = '';
            document.body.style.userSelect = '';
            document.querySelectorAll('iframe, webview, .xterm').forEach(el => el.style.pointerEvents = '');
            document.removeEventListener('mousemove', onMove);
            document.removeEventListener('mouseup', onUp);
        };
        document.addEventListener('mousemove', onMove);
        document.addEventListener('mouseup', onUp);
    });
}

function initResizeLocks() {
    const lockPairs = [
        { btnId: 'lock-sidebar', handleId: 'sidebar-resize', key: 'sidebar' },
        { btnId: 'lock-chat',    handleId: 'chat-resize',    key: 'chat' },
        { btnId: 'lock-panel',   handleId: 'panel-resize',   key: 'panel' },
    ];

    lockPairs.forEach(({ btnId, handleId, key }) => {
        const btn = document.getElementById(btnId);
        const handle = document.getElementById(handleId);
        if (!btn || !handle) return;

        btn.addEventListener('click', (e) => {
            e.stopPropagation(); // Don't trigger resize drag
            resizeLocks[key] = !resizeLocks[key];
            const locked = resizeLocks[key];

            // Update button appearance
            btn.textContent = locked ? '🔒' : '🔓';
            btn.classList.toggle('locked', locked);
            btn.classList.toggle('visible', locked); // Keep visible when locked

            // Update handle appearance
            handle.classList.toggle('locked', locked);

            // Toast notification
            const names = { sidebar: 'Sidebar', chat: 'Chat Panel', panel: 'Terminal' };
            if (typeof showToast === 'function') {
                showToast(`${names[key]} resize ${locked ? 'locked 🔒' : 'unlocked 🔓'}`, locked ? 'warning' : 'info');
            }
        });
    });
}

function fitAllTerminals() {
    for (const [_, info] of state.terminal.instances) {
        try { info.fitAddon.fit(); } catch { }
    }
}

